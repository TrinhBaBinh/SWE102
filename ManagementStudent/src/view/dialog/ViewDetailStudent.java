    private void btnRemoveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRemoveMouseClicked
        // TODO add your handling code here:
        txtTitle.setText("Remove");
        int choice = new StudentBo().choiceFunction("Question ", "Do you remove student?", "Yes", "No");
        switch (choice) {
            case 0: {
                boolean test = new StudentDao().delete(txtRollNumber.getText());
                if (test) {
                    JOptionPane.showMessageDialog(null, "Remove success");
                } else {
                    JOptionPane.showMessageDialog(null, "Remove Fail");
                }
                this.dispose();
                break;
            }
            case 1: {
                break;
            }
        }
    }//GEN-LAST:event_btnRemoveMouseClicked

