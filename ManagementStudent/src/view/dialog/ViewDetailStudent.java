    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        // TODO add your handling code here:
        txtTitle.setText("Update");
        int number = new StudentBo().choiceFunction("Update Student", "Do you want to update student "
                + this.student.getFullName() + " ?", "Yes", "No");

        switch (number) {
            case 0: {
                String name = Validate.getName(txtFullName);
                boolean gender = jRadioButton3.isSelected();
                String email = Validate.getEmail(txtEmail);
                String phoneNumber = Validate.getPhoneNumber(txtPhoneNumber);
                String address = txtAddress.getText();
                int id = this.student.getId();
                Student newStudent = new Student(id, txtRollNumber.getText(), name, gender, email, phoneNumber, address);
                String mess = new StudentBo().update(newStudent, id);
                JOptionPane.showMessageDialog(null, mess);
                this.dispose();
                break;
            }
            case 1: {
                break;
            }
        }
    }//GEN-LAST:event_btnUpdateMouseClicked

